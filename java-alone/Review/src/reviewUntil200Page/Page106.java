package reviewUntil200Page;

public class Page106 {
	public static void main(String[] args){
		int value1 = Integer.parseInt("10");//문자열 10을 정수형 10으로 바꾸겠다.
		double value2 = Double.parseDouble("3.14");//문자열 3.14를 실수형 더블타입. 3.14로 바꾸겠다.
		boolean value3 = Boolean.parseBoolean("true");
		
		System.out.println("value1: " + value1);
		System.out.println("value2: " + value2);
		System.out.println("value3: " + value3);
		
		String str1 = String.valueOf(10); //10을 문자열 10으로 바꾸겠다.
		String str2 = String.valueOf(3.14); //3.14를 문자열 3.14로 바꾸겠다.
		String str3 = String.valueOf(true); 
		
		System.out.println("str1: " + str1);
		System.out.println("str2: " + str2);
		System.out.println("str3: " + str3);
		
	}
}
